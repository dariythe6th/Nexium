function translatePage(language) {
  const langData = translations[language];
  if (!langData) return;

  // Переводим title страницы
  const titleKey = document.querySelector('title').getAttribute('data-translate');
  if (titleKey && langData[titleKey]) {
    document.title = langData[titleKey];
  }

  // Переводим все элементы с data-translate
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (langData[key]) {
      element.textContent = langData[key];
    }
  });

  // Переводим placeholder'ы
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    if (langData[key]) {
      element.placeholder = langData[key];
    }
  });

  // Обновляем атрибут lang у html
  document.documentElement.lang = language;
}

// При загрузке страницы
document.addEventListener('DOMContentLoaded', function() {
  const currentLanguage = localStorage.getItem('siteLanguage') || 'ru';
  translatePage(currentLanguage);
});


